package place;

public class Member {
	private int id;
	private String name;
	
	public int getId() {
		return id;
	}
	public int setId(int id) {
		this.id = id;
		return id;
	}
	public String getName() {
		return name;
	}
	public String setName(String name) {
		this.name = name;
		return name;
	}
	
	
}
